namespace sh
{
	namespace math
	{
		//! Constant for PI.
		static const float k_pi = 3.14159265359f;

		//! Constant for half of PI.
		static const float k_pi2 = k_pi * 0.5f;

		//! Constant for two PI.
		static const float k_2pi = k_pi * 2.0f;

		//! Constant for 3 * PI / 2.
		static const float k_3pi2 = 3.0f * k_pi2;
	}
}
